        "ATTACH",
        "  `message attach` adds one or more GSV filesystem files to this run's",
        "  automatic final response. It does not send a second message. Files are",
        "  staged as process-owned media; an existing path under this process's",
        "  /var/media directory is reused. --mime overrides the inferred type for",
        "  a single file.",
